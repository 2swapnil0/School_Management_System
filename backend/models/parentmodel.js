import mongoose from "mongoose";

const ParentSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
});

const Parents = mongoose.model('Parent', ParentSchema);

export default Parents;
